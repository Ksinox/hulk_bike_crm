-- Новый тип платежа deposit_forfeit — «зачёт залога в счёт ущерба».
-- Когда залог удержан под ущерб (damage_reports.deposit_covered), он перестаёт
-- быть возвратным и становится ВЫРУЧКОЙ. Раньше эта сумма нигде не учитывалась
-- как доход (только уменьшала долг), поэтому выручка занижалась на размер
-- удержанного залога. Теперь под неё создаётся отдельный платёж этого типа.
-- На расчёт долга НЕ влияет (долг считается по deposit_covered + damage-платежам).
ALTER TYPE "payment_type" ADD VALUE IF NOT EXISTS 'deposit_forfeit';
