-- v0.4.43 — устраняем дублирование «ремонт» между двумя сущностями.
--
-- Раньше:
--   • repair_jobs — настоящий ремонт с чек-листом, фотографиями, статусом
--   • scooter_maintenance.kind='repair' — расходная запись с пометкой «ремонт»
-- Это две разные вещи под одинаковым названием — путаница в CRM.
--
-- Решение: scooter_maintenance остаётся ТОЛЬКО для расходов
-- (oil/parts/other). Существующие записи с kind='repair' конвертируем
-- в 'other', добавив в note маркер «(был тип Ремонт)» — чтобы не
-- потерять исходный смысл записи.
--
-- Идемпотентно: повторный запуск не меняет ничего, т.к. после первой
-- конвертации записей с kind='repair' больше нет.

UPDATE scooter_maintenance
   SET kind = 'other',
       note = CASE
         WHEN note IS NULL OR note = '' THEN '(был тип: Ремонт)'
         ELSE '(был тип: Ремонт) ' || note
       END
 WHERE kind = 'repair';
