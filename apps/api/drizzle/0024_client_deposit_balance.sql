-- v0.3.9 — баланс депозита у клиента.
--
-- Депозит — «неиспользованные средства, которые подтянутся в счёт оплаты
-- следующей аренды». Не тот залог, что под скутер (rentals.deposit) —
-- это другое: переплата клиента, которая остаётся «на будущее».
--
-- Например клиент платит 1800₽, текущий тариф 500₽/сут × 3 дня = 1500₽,
-- остаток 300₽ — в депозит. При следующей оплате 300₽ из депозита
-- автоматически зачитываются.
--
-- История изменений живёт в activity_log с action='deposit_changed'.
-- Здесь — только текущий баланс (иначе плодим JOIN'ы на каждый чих).

DO $$ BEGIN
  ALTER TABLE "clients"
    ADD COLUMN "deposit_balance" integer NOT NULL DEFAULT 0;
EXCEPTION WHEN duplicate_column THEN NULL; END $$;
