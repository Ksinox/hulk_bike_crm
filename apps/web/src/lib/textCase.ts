/**
 * Утилиты по приведению строкового регистра.
 *
 * Используем `toLocale*Case('ru-RU')` чтобы корректно работать с
 * кириллицей: ё → Ё, й → Й. Стандартные методы это и так умеют по UCD,
 * но локаль-вариант надёжнее.
 */

export function toUpperRu(s: string): string {
  return s.toLocaleUpperCase("ru-RU");
}

/**
 * Каждое слово — с большой буквы, остальные строчные. «иванов иван
 * иванович» → «Иванов Иван Иванович». «г.москва ул.ленина» → «Г.Москва
 * Ул.Ленина». Дефис тоже считается разделителем слов: «иван-петров»
 * → «Иван-Петров».
 *
 * Под «словом» понимается последовательность букв любого алфавита
 * (\p{L}). Цифры и знаки препинания не трогаем.
 */
export function toTitleCaseRu(s: string): string {
  return s.replace(/(\p{L})(\p{L}*)/gu, (_, first: string, rest: string) => {
    return first.toLocaleUpperCase("ru-RU") + rest.toLocaleLowerCase("ru-RU");
  });
}
